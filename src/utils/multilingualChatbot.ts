export type Language = 'en' | 'hi' | 'bn';

// Helper function to get random response
export function getRandomResponse(responses: string[]): string {
  return responses[Math.floor(Math.random() * responses.length)];
}

// Helper function to translate disease name
export function translateDisease(disease: string, language: Language): string {
  return diseaseTranslations[language][disease] || disease;
}

// Helper function to translate symptom
export function translateSymptom(symptom: string, language: Language): string {
  const lowerSymptom = symptom.toLowerCase();
  return symptomTranslations[language][lowerSymptom] || symptom;
}
